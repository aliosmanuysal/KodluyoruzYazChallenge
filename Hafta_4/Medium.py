# Ürün fiyatları
kitap_fiyat = 80
defter_fiyat = 20
kalem_fiyat = 5

# Satın alınan ürün miktarları
kitap_miktar = 2
defter_miktar = 3
kalem_miktar = 4

# Toplam ödeme hesaplama
toplam_odeme = (kitap_fiyat * kitap_miktar) + (defter_fiyat * defter_miktar) + (kalem_fiyat * kalem_miktar)

print("Müşteri ödemesi gereken tutar:", toplam_odeme, "TL")
